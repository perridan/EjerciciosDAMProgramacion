import java.util.Scanner;

class Ejercicio7 {
  public static void main(String[] args) {

    // vamos a hacer un bucle del 1 al 100
    for (int i = 5; i <= 50; i++) {
      // si el modulo de la division por 3 es 0 es multiplo de 3
      if (i % 3 == 0) {
        System.out.println(i);

      }

    }

  }

}